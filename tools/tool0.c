void	*ft_malloc(size_t size)
{
	void	*ptr;
	t_list	*tmp;

	ptr = malloc(size);
	if (!ptr)
	{
		ft_putstr_fd("Error : malloc fail to allocate", 2);
		ft_lstclear(&g_gbc, free);
		exit(1);
	}
	tmp = ft_lstnew_custom(ptr);
	if (!tmp)
	{
		ft_putstr_fd("Error : malloc fail to allocate", 2);
		free(ptr);
		ft_lstclear(&g_gbc, free);
		exit(1);
	}
	ft_lstadd_back(&g_gbc, tmp);
	return (ptr);
}
